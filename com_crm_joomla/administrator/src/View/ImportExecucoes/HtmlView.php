namespace Joomla\Component\Crm\Administrator\View\ImportExecucoes;

use Joomla\CMS\MVC\View\ListView;
use Joomla\CMS\Toolbar\ToolbarHelper;
use Joomla\CMS\Language\Text;
use Joomla\CMS\Factory;

/**
 * ImportExecucoes View
 *
 * @since  1.0.0
 */
class HtmlView extends ListView
{
    /**
     * Display the view
     */
    public function display($tpl = null)
    {
        $this->items = $this->get('Items');
        $this->pagination = $this->get('Pagination');
        $this->state = $this->get('State');

        // Check for errors.
        if (count($errors = $this->get('Errors'))) {
            Factory::getApplication()->enqueueMessage(implode("\n", $errors), 'error');
            return;
        }

        $this->addToolbar();

        parent::display($tpl);
    }

    /**
     * Add the page title and toolbar.
     */
    protected function addToolbar()
    {
        ToolbarHelper::title(Text::_('COM_CRM_IMPORTEXECUCOES_VIEW_DEFAULT_TITLE'));
        ToolbarHelper::deleteList(Text::_('COM_CRM_CONFIRM_DELETE'), 'importexecucoes.delete');
    }
}
